class Pokemon {
    type1;
    type2;
    ability;
    // stats
    attack;
    defense;
    sattack;
    sdefense;
    constructor(type1, type2, ability, attack, defense, sattack, sdefense)
    {
        this.type1 = type1;
        this.type2 = type2;
        this.ability = ability;
        this.attack = attack;
        this.defense = defense;
        this.sattack = sattack;
        this.sdefense = sdefense;
    }
}